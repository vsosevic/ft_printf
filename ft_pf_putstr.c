#include "ft_printf.h"

int	ft_pf_putstr(char *s)
{
	int i;

	if (s)
	{
		i = 0;
		while (s[i] != '\0')
		{
			ft_putchar(s[i]);
			i++;
		}
	}
	return (i);
}
